-- Date: 2026-04-07
--
-- Adds:
--   1. Async queue + logs for WA API delivery attempts
--   2. Helper functions to normalize JID + enqueue notifications
--   3. Triggers for:
--      - majun_transactions  -> notify tailor
--      - percas_stock        -> notify manager
--      - expeditions         -> notify manager
--   4. dequeue_wa_notifications RPC for queue workers
